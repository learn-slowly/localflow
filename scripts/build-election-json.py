def build_final_data():
    # API 데이터 로드
    api_path = os.path.join(OUT_DIR, "jinju-elections-api.json")
    with open(api_path, "r", encoding="utf-8") as f:
        api_data = json.load(f)

    final = []

    for elec in api_data:
        sg_id = elec["sgId"]
        sg_type = elec["sgTypecode"]
        key = f"{sg_id}_{sg_type}"

        entry = {
            "sgId": sg_id,
            "label": elec["label"],
            "date": elec["date"],
            "results": elec["results"],
            "votersByDong": elec["votersByDong"],
            "dongResults": [],
        }

        # 읍면동별 선거인수를 dict로 변환
        voters_map = {d["dong"]: d["voters"] for d in elec["votersByDong"]}

        # 나무위키 득표율 데이터가 있으면 읍면동별 결과 생성
        rate_data = DONG_RATE_MAP.get(key)
        if rate_data:
            for result in elec["results"]:
                district = result["district"]
                # "all"이면 전체 읍면동, 아니면 해당 선거구 읍면동만
                rates = rate_data.get("all") or rate_data.get(district, {})
                if not rates:
                    continue

                # 해당 선거구의 읍면동 목록
                if district in ASSEMBLY_DISTRICTS:
                    dongs = ASSEMBLY_DISTRICTS[district]
                else:
                    dongs = ALL_DONGS

                for dong in dongs:
                    dong_rates = rates.get(dong)
                    if not dong_rates:
                        continue

                    dong_entry = {
                        "dong": dong,
                        "district": district,
                        "voters": voters_map.get(dong, 0),
                        "rates": dong_rates,
                    }
                    entry["dongResults"].append(dong_entry)

        final.append(entry)

    # 최종 저장
    out_path = os.path.join(OUT_DIR, "jinju-elections.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(final, f, ensure_ascii=False, indent=2)

    print(f"저장: {out_path}")
    for e in final:
        n_results = len(e["results"])
        n_dongs = len(e["dongResults"])
        n_voters = len(e["votersByDong"])
        print(f"  {e['label']}: 선거구 {n_results}개, 읍면동 선거인수 {n_voters}개, 읍면동 득표율 {n_dongs}개")


if __name__ == "__main__":
    build_final_data()
